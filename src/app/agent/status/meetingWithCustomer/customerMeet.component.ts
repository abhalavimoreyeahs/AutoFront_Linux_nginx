import { Component } from '@angular/core';
import { map } from 'rxjs/operators';
import { Breakpoints, BreakpointObserver } from '@angular/cdk/layout';

@Component({
  selector: 'app-customerMeet',
  templateUrl: './customerMeet.component.html',
  styleUrls: ['./customerMeet.component.css']
})
export class CustomerMeetComponent {
  /** Based on the screen size, switch from standard to one column per row */
  cards = this.breakpointObserver.observe(Breakpoints.Handset).pipe(
    map(({ matches }) => {
      if (matches) {
        return [
          { title: 'Card 1', cols: 1, rows: 1 },
          { title: 'Card 2', cols: 1, rows: 1 },
          { title: 'Card 3', cols: 1, rows: 1 },
          { title: 'Card 4', cols: 1, rows: 1 }
        ];
      }

      return [
        { title: 'Card 1', cols: 2, rows: 1 },
        { title: 'Card 2', cols: 1, rows: 1 },
        { title: 'Card 3', cols: 1, rows: 2 },
        { title: 'Card 4', cols: 1, rows: 1 }
      ];
    })
  );

  constructor(private breakpointObserver: BreakpointObserver) {}

  AgentStatus = [
    {value: '0', name: 'Active'},
    {value: '1', name: 'Callback'},
    {value: '2', name: 'Manual Dial'},
    {value: '3', name: 'Break'},
    {value: '4', name: 'Training'},
    {value: '5', name: 'Meeting With Customer'}
  ];
}
